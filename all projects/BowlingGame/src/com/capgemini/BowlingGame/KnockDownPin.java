package com.capgemini.BowlingGame;

public class KnockDownPin {

	int pin;

	public KnockDownPin(int pin) {
		
		this.pin = pin;
	}

	public int getPin() {
		return pin;
	}

	public void setPin(int pin) {
		this.pin = pin;
	}
	
	
	
	

	@Override
	public String toString() {
		return "KnockDownPin [pin=" + pin + "]";
	}

	
	
}
